    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String identificador = this.indentificadorText.getText();
        String tempoCasa = this.tempoCasaText.getText();
        String tempoBar = this.tempoBarText.getText();
        System.out.println(identificador + tempoCasa + tempoBar);
        float tempoCasaFloat = 0;
        float tempoBarFloat = 0;
        try {
            tempoCasaFloat = Float.parseFloat(tempoCasa);
            tempoBarFloat = Float.parseFloat(tempoBar);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Tempo Bar e Casa devem ser floats", "Alerta!!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        this.father.createCliente(identificador, tempoCasaFloat, tempoBarFloat);
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

